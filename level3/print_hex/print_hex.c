#include <unistd.h>

int		main(int argc, char **argv)
{
	int n;
	int i;
	char hex[16] = "0123456789abcdef";

	n = 0;
	i = 1;
	if (argc == 2)
	{
		argv++;
		while (**argv)
		{
			n = n * 10 + (**argv - '0');
			(*argv)++;
		}
		while (n / i > 15)
			i *= 16;
		while (i > 0)
		{
			write(1, &hex[n / i], 1);
			n %= i;
			i /= 16; 
		}
	}
	write(1, "\n", 1);
	return (0);
}
